A, gen, frob := M5A();
F<al> := BaseRing(A);
bt := (5*al-1)/8;

assert al-bt eq (3*al+1)/8;
// al can't be -1/3 as then al = bt

// Check general properties

u0_1 := 3*bt*A.1 - 2*(A.3+A.4) -bt/(al-bt)*A.6;
u0_2 := (A.2+A.5) - (A.3+A.4) -bt/(al-bt)*A.6;

v_al := A.2+A.5-(A.3+A.4) +A.6;

w_bt_1 := A.2-A.5;
w_bt_2 := A.3-A.4;

assert u0_1*A.1 eq A!0;
assert u0_2*A.1 eq A!0;

assert v_al*A.1 eq al*v_al;

assert w_bt_1*A.1 eq bt*w_bt_1;
assert w_bt_2*A.1 eq bt*w_bt_2;

// Identity
so, id := HasOne(A);
assert so;
assert id eq 1/5/(al-bt)*A![1,1,1,1,1,0];
// So the algebra always has an identity if the characteristic is not 5

// No subalgebras to check

// Now check for ideals

assert frob[1,2] eq 3/4*bt;
// So the projection graph always has an edge between a_0 and a_1 since bt ne 0

assert Determinant(frob) eq -5^6/2^23*(al-bt)^2*(3*al-7)^5/bt;
// Since al \neq bt and the characteristic can't be 2
// the only possibilities are if the characteristic is 5, or al = 7/3

// We deal with al = 7/3, char not 5 below.

// See bottom for proof that 5A \cong IY_5 in char 5.
//  Look in IY5 file for char 5 case.
// NB value of al makes no difference.

// ---------------------------------------------------
//
// For al = 7/3
//
// ---------------------------------------------------
A, gen, frob := M5A(7/3);

// The radical is 5-dimensional
assert Dimension(NullSpace(frob)) eq 5;
P<t> := PolynomialRing(QQ);
p := CharacteristicPolynomial(frob);
assert p eq t^5*(t-5);
// so the nullspace is 5-dimensional in any characteristic

R := ideal<A| [A!x : x in Basis(Nullspace(frob))]>;

u1 := A.1-A.5;
u2 := A.2-A.5;
u3 := A.3-A.5;
u4 := A.4-A.5;
u5 := A.6;
assert u1 in R and u2 in R and u3 in R and u4 in R and u5 in R;

// In ordinary representation theory, R has G-module structure 1+4, so just need to check the trivial submodule and the irreducible module generated by the difference of axes

assert Dimension(ideal<A | u1>) eq 5;
assert A.2*u1 eq 4/3*u1 + 2/3*u5;

assert Dimension(ideal<A | u5>) eq 5;
assert A.1*u5 eq u2-u3-u4+u5;
// So no subideals of R


