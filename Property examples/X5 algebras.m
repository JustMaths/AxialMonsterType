A, gen, frob := M5A();
F<al> := BaseRing(A);
bt := (5*al-1)/8;

assert al-bt eq (3*al+1)/8;
// al can't be -1/3 as then al = bt

// Check general properties

u0_1 := 3*bt*A.1 - 2*(A.3+A.4) -bt/(al-bt)*A.6;
u0_2 := (A.2+A.5) - (A.3+A.4) -bt/(al-bt)*A.6;

v_al := A.2+A.5-(A.3+A.4) +A.6;

w_bt_1 := A.2-A.5;
w_bt_2 := A.3-A.4;

assert u0_1*A.1 eq A!0;
assert u0_2*A.1 eq A!0;

assert v_al*A.1 eq al*v_al;

assert w_bt_1*A.1 eq bt*w_bt_1;
assert w_bt_2*A.1 eq bt*w_bt_2;

// Identity
so, id := HasOne(A);
assert so;
assert id eq 1/5/(al-bt)*A![1,1,1,1,1,0];
// So the algebra always has an identity if the characteristic is not 5

// No subalgebras to check
// See below in idempotents for additional automorphism

// ---------------------------------------------------
//
//     Ideals
//
// ---------------------------------------------------

assert frob[1,2] eq 3/4*bt;
// So the projection graph always has an edge between a_0 and a_1 since bt ne 0

assert Determinant(frob) eq -5^6/2^23*(al-bt)^2*(3*al-7)^5/bt;
// Since al \neq bt and the characteristic can't be 2
// the only possibilities are if the characteristic is 5, or al = 7/3

// We deal with al = 7/3, char not 5 below.

