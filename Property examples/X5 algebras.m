// Theorem: IY5 and M5A are isomorphic in characteristic 5

A, gen, frob := M5A(: base_ring := GF(5));

AY := V2(: base_ring := GF(5));

// Checking the 6th basis vector

assert AY.6 eq AY.1*AY.2 - 3*(AY.1+AY.2);

// So set
v := A.1*A.2 - 3*(A.1+A.2);

bas := [ A.i : i in [1..5]] cat [v];
AA  := ChangeBasis(A, bas);

// Checking if the multiplication is the same

multA := [[Eltseq(v): v in R]: R in BasisProducts(AA)];
multAY := [[Eltseq(v): v in R]: R in BasisProducts(AY)];

assert multA eq multAY;
